package OCT.if_loop_Condition;

public class ex1_if_loop {
    public static void main(String[] args) {

        boolean b = !true;   //! represent not (!true)-- false
        if (b)
        {
            System.out.println("B is true");
        }
        else
        {
            System.out.println("B is not true");
        }
    }
}
