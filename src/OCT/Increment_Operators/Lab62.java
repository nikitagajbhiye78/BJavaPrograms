package OCT.Increment_Operators;

public class Lab62 {
    public static void main(String[] args) {

        //Prefix operator

        int a= 100;
        int b= ++a;  // Prefix operators increases value by 1 and then stored
        System.out.println(b);
                     // value of a will be increased too
        System.out.println(a);


    }
}
